import telebot
import os
import time
import random

BOT_TOKEN = os.environ.get('BOT_TOKEN')
CHAT_ID = "@Norham313"

bot = telebot.TeleBot(BOT_TOKEN)

# 📖 آيات قرآنية
quran = [
    "﴿إِنَّ مَعَ الْعُسْرِ يُسْرًا﴾",
    "﴿وَرَحْمَتِي وَسِعَتْ كُلَّ شَيْءٍ﴾",
    "﴿وَاصْبِرْ فَإِنَّ اللَّهَ لَا يُضِيعُ أَجْرَ الْمُحْسِنِينَ﴾"
]

# 🕌 أحاديث من مصادر شيعية
hadith = [
    "قال الإمام علي (ع): الناس صنفان إما أخ لك في الدين أو نظير لك في الخلق\n📚 نهج البلاغة",
    
    "قال الإمام الصادق (ع): من حسن خلقه كثر محبوه\n📚 الكافي – الشيخ الكليني",

    "قال رسول الله (ص): إنما الأعمال بالنيات\n📚 بحار الأنوار",

    "قال الإمام الباقر (ع): أفضل العبادة الصبر\n📚 الكافي"
]

# 🤲 أذكار
adhkar = [
    "سبحان الله وبحمده سبحان الله العظيم",
    "أستغفر الله ربي وأتوب إليه",
    "لا إله إلا الله وحده لا شريك له",
    "اللهم صلِّ على محمد وآل محمد"
]

# 📚 قصص دينية
stories = [
    "كان أحد الصالحين يكثر من الصدقة فبارك الله له في رزقه\n📚 روايات أهل البيت (ع)",

    "رجل برّ والديه ففتح الله له أبواب الخير\n📚 بحار الأنوار",

    "الإمام علي (ع) كان يطعم الفقراء ليلاً دون أن يعرفه أحد\n📚 نهج البلاغة"
]

# 💡 حكم
wisdom = [
    "الصبر مفتاح الفرج",
    "من توكل على الله كفاه",
    "النية الصالحة تصنع المعجزات",
    "لا راحة مع الحقد"
]

def get_random_content():
    all_content = quran + hadith + adhkar + stories + wisdom
    return random.choice(all_content)

def send_post():
    try:
        msg = f"""🌿 صدقة جارية عن روح والدي كاظم صالح خليفة 🤍

{get_random_content()}

نسألكم الدعاء وقراءة الفاتحة مع الصلاة على محمد وآل محمد 🌸"""

        bot.send_message(CHAT_ID, msg)
        print("تم نشر منشور")

    except Exception as e:
        print(e)

# ⏳ نشر كل 3 ساعات
while True:
    send_post()
    time.sleep(3 * 60 * 60)
