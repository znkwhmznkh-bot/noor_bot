def get_current_index():
    # دالة لقراءة المؤشر الحالي من الملف لضمان الترتيب التسلسلي #
    if not os.path.exists('counter.txt'):
        return 0
    with open('counter.txt', 'r') as f:
        try:
            return int(f.read().strip())
        except:
            return 0

def update_index(next_index):
    # دالة لتحديث المؤشر وحفظه للمرة القادمة #
    with open('counter.txt', 'w') as f:
        f.write(str(next_index))

def send_post():
    try:
        current_index = get_current_index()
        
        # التأكد من أن المؤشر لا يتجاوز حجم القائمة #
        if current_index >= len(messages):
            current_index = 0
            
        # اختيار المنشور الحالي بالتسلسل #
        content = messages[current_index]
        
        # إرسال المحتوى مع التذيل الثابت #
        bot.send_message(CHANNEL_ID, content + F)
        print(f"Success! Post {current_index + 1} sent to channel.")
        
        # تحديث المؤشر للمنشور التالي (وإعادته للصفر إذا انتهت القائمة) #
        next_index = (current_index + 1) % len(messages)
        update_index(next_index)
        
    except Exception as e:
        print(f"Error: {e}")

if __name__ == "__main__":
    send_post()
