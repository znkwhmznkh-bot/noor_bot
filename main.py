import telebot
import os
import random

# إعدادات البوت
BOT_TOKEN = os.environ.get('BOT_TOKEN')
CHAT_ID = "-1002302305574"

bot = telebot.TeleBot(BOT_TOKEN)

# قائمة بسيطة من الأذكار والأحاديث (يمكنك زيادتها لاحقاً)
messages = [
    "قال ﷺ: (خياركم من تعلم القرآن وعلمه).\n\nصدقة جارية عن روح المرحوم كاظم صالح خليفة. نسألكم الفاتحة.",
    "قال ﷺ: (كلمتان خفيفتان على اللسان، ثقيلتان في الميزان: سبحان الله وبحمده، سبحان الله العظيم).\n\nصدقة جارية عن روح المرحوم كاظم صالح خليفة.",
    "اللهم اغفر للمؤمنين والمؤمنات، الأحياء منهم والأموات.\n\nصدقة جارية عن روح المرحوم كاظم صالح خليفة. نسألكم الدعاء.",
    "سبحان الله، والحمد لله، ولا إله إلا الله، والله أكبر.\n\nصدقة جارية عن روح المرحوم كاظم صالح خليفة."
]

def send_post():
    try:
        # اختيار رسالة عشوائية من القائمة
        text = random.choice(messages)
        bot.send_message(CHAT_ID, text)
        print("✅ تم النشر بنجاح في القناة!")
    except Exception as e:
        print(f"❌ حدث خطأ أثناء النشر: {e}")

if __name__ == "__main__":
    send_post()
